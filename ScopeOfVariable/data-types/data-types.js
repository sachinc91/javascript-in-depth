// String:
let stringDemo = "A string of text.";
console.log("String:", stringDemo);
console.log("String is typeof ::", typeof stringDemo);

// Numbes:
let integerDemo = 4;
console.log("Integer:", integerDemo);
console.log("Integer is typeof ::", typeof integerDemo);

let floatDemo = 5.6;
console.log("Floating point number:", floatDemo);
console.log("Floating point number is typeof ::", typeof floatDemo);

// Boolean:
let booleanDemo = true;
console.log("Boolean value is typeof ::", booleanDemo);
console.log("Boolean value is typeof ::", typeof booleanDemo);

// Null value (nothing):
let nullDemo = null;
console.log("Null value:", nullDemo);
console.log("Null value is typeof ::", nullDemo);


// Undefined:
let undefinedDemo;
console.log("Undefined: ", undefinedDemo);
console.log("Undefined is typeof :: ", typeof undefinedDemo);

let undefinedAssignedDemo = undefined;
console.log("Undefined assigned:", undefinedAssignedDemo);
console.log("Undefined assigned is typeof :: ", typeof undefinedAssignedDemo);

// Object:
const objectDemo = {
    dance: "Mambo",
    number: 5,
};
console.log("Object: ", objectDemo);
console.log("Object is typeof :: ", typeof objectDemo);

// Array:
const arrayDemo = ["tango", "foxtrot", "waltz", "rumba", "bolero"];
console.log("Array is typeof :: ", typeof arrayDemo);